// Port the homepage template into src/pages/index.astro.
// Strips header/footer (BaseLayout supplies them); injects style + body + scripts.
// Run: node scripts/port-homepage.mjs

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');
const TEMPLATE = path.resolve(
  ROOT, '..', 'HTML-Templates',
  'RoofPicker-Latest-HomePage-Vizualizer-5-13-26.html',
);
const OUT = path.resolve(ROOT, 'src', 'pages', 'index.astro');

function pickStyleBlock(html) {
  const m = html.match(/<style\b[^>]*>([\s\S]*?)<\/style>/);
  return m ? m[1] : '';
}

function pickBody(html) {
  const m = html.match(/<body[^>]*>([\s\S]*?)<\/body>/);
  return m ? m[1] : '';
}

function stripHeaderFooterHtml(body) {
  // Strip site header/footer/nav-v2 + body scripts (re-injected below).
  // Duplicate scripts would register every handler twice.
  let out = body;
  out = out.replace(/<header\b[^>]*class=["'][^"']*site[^"']*["'][^>]*>[\s\S]*?<\/header>/g, '');
  out = out.replace(/<footer\b[^>]*class=["'][^"']*site[^"']*["'][^>]*>[\s\S]*?<\/footer>/g, '');
  out = out.replace(/<nav\b[^>]*class=["'][^"']*nav-v2[^"']*["'][^>]*>[\s\S]*?<\/nav>/g, '');
  out = out.replace(/<script\b[^>]*>[\s\S]*?<\/script>/g, '');
  return out;
}

function stripHeaderFooterCss(css) {
  // Drop selectors targeting site nav/footer; walk balanced braces.
  const KEEP = (selector) => {
    const s = selector.trim();
    if (!s) return false;
    if (/header\.site\b/.test(s)) return false;
    if (/footer\.site\b/.test(s)) return false;
    if (/^\.(nav|nav-links|nav-cta|wordmark|foot-inner|foot-nav|foot-copy)\b/.test(s)) return false;
    return true;
  };

  const out = [];
  let i = 0;
  while (i < css.length) {
    while (i < css.length && /\s/.test(css[i])) { out.push(css[i]); i++; }
    if (css.startsWith('/*', i)) {
      const end = css.indexOf('*/', i + 2);
      const stop = end === -1 ? css.length : end + 2;
      out.push(css.slice(i, stop));
      i = stop;
      continue;
    }
    if (css.startsWith('@', i)) {
      let j = i;
      while (j < css.length && css[j] !== ';' && css[j] !== '{') j++;
      if (css[j] === ';') {
        out.push(css.slice(i, j + 1));
        i = j + 1;
        continue;
      }
      // @media / @keyframes etc. — keep block as-is.
      let depth = 0;
      let k = j;
      do {
        if (css[k] === '{') depth++;
        else if (css[k] === '}') depth--;
        k++;
      } while (k < css.length && depth > 0);
      out.push(css.slice(i, k));
      i = k;
      continue;
    }
    const braceIdx = css.indexOf('{', i);
    if (braceIdx === -1) {
      out.push(css.slice(i));
      break;
    }
    const selector = css.slice(i, braceIdx);
    const kept = selector.split(',').map((s) => s.trim()).filter(KEEP).join(', ');
    let depth = 1;
    let k = braceIdx + 1;
    while (k < css.length && depth > 0) {
      if (css[k] === '{') depth++;
      else if (css[k] === '}') depth--;
      k++;
    }
    if (kept) {
      out.push(kept + ' ' + css.slice(braceIdx, k));
    }
    i = k;
  }
  return out.join('');
}

function pickInlineScripts(html) {
  const body = (html.match(/<body[^>]*>([\s\S]*?)<\/body>/) || [, ''])[1];
  const blocks = [];
  const re = /<script\b([^>]*)>([\s\S]*?)<\/script>/g;
  let m;
  while ((m = re.exec(body)) !== null) {
    const attrs = m[1];
    // Skip JSON-LD (head only).
    if (/type=["']application\/ld\+json["']/.test(attrs)) continue;
    blocks.push(m[2]);
  }
  return blocks.join('\n\n/* ---- next inline script ---- */\n\n');
}

function escape(str) {
  // Escape backticks + ${} for wrapping in a JS template literal.
  return str
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

async function main() {
  const html = await fs.readFile(TEMPLATE, 'utf-8');
  let css = stripHeaderFooterCss(pickStyleBlock(html));
  const body = stripHeaderFooterHtml(pickBody(html)).trim();
  const scripts = pickInlineScripts(html);

  // Override resets the hero's nav-v2 margin so H1 sits below
  // BaseLayout's fixed transparent NavBar.
  css += `

/* === Astro overlay overrides — applied because the in-hero nav-v2 is
       stripped and replaced by BaseLayout's fixed transparent NavBar. */
.hero-v2-content { margin-top: 0; padding-top: 80px; }
.hero-v2-header  { padding-top: 0; }
`;

  const astro = `---
// AUTO-GENERATED by scripts/port-homepage.mjs. Re-run: npm run port-homepage

import BaseLayout from '@layouts/BaseLayout.astro';
import { softwareApplication, faqPage } from '@lib/schema';

const css = ${'`'}${escape(css)}${'`'};
const body = ${'`'}${escape(body)}${'`'};
const inlineScripts = ${'`'}${escape(scripts)}${'`'};

const homepageFaqs = [
  { q: 'Is the visualizer free?', a: 'Yes. No signup required.' },
  { q: 'Does it work on phones?', a: 'Yes. Phone photos are the primary input.' },
  { q: 'Which brands are supported?', a: 'GAF, CertainTeed, IKO, TAMKO, Owens Corning, Malarkey, and Atlas.' },
];
---
<BaseLayout
  title="RoofPicker — See your roof. Before you buy it."
  description="Free roof color visualizer. Upload a photo, swap through real shingle SKUs in 4 seconds. No signup."
  canonical="/"
  jsonLd={[softwareApplication(), faqPage(homepageFaqs)]}
  transparentNav={true}
>
  <style is:global set:html={css}></style>
  <Fragment set:html={body} />
  <script is:inline set:html={inlineScripts}></script>
</BaseLayout>
`;
  await fs.writeFile(OUT, astro, 'utf-8');
  console.log(`Wrote ${OUT}`);
  console.log(`  css length    : ${css.length}`);
  console.log(`  body length   : ${body.length}`);
  console.log(`  script length : ${scripts.length}`);
}

main().catch((err) => { console.error(err); process.exit(1); });
